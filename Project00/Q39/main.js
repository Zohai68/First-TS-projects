function city_country(city, country) {
    return "".concat(city, ", ").concat(country);
}
var city1 = city_country("karachi", "Pakistan");
var city2 = city_country("lahore", "Pakistan");
var city3 = city_country("singapore", "South Asia");
console.log(city1);
console.log(city2);
console.log(city3);
